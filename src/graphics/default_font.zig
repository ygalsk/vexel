/// Atlas layout: 16 columns × 8 rows of 8×8 glyphs = 128×64 pixels.
/// Glyphs 0x20..0x7F (96 chars) occupy slots 0..95.
const ATLAS_COLS: u16 = 16;
const GLYPH_W: u8 = 8;
const GLYPH_H: u8 = 8;
const ATLAS_W: u16 = ATLAS_COLS * GLYPH_W;
const ATLAS_H: u16 = 8 * GLYPH_H; // ceil(96/16) = 6, but 8 rows fits cleanly

/// Build a FontAtlas from the embedded CP437 bit patterns.
/// Each set bit becomes a white (255,255,255,255) pixel; clear bits are transparent.
pub fn createDefaultFont(allocator: std.mem.Allocator) !FontAtlas {
    const atlas_pixels = try allocator.alloc(u8, @as(usize, ATLAS_W) * @as(usize, ATLAS_H) * 4);
    defer allocator.free(atlas_pixels); // loadFromBitmap dupes — free the staging buffer after
    @memset(atlas_pixels, 0);

    for (0..96) |i| {
        const cp: u8 = @intCast(0x20 + i);
        const bits = font_data[cp];

        const col: u16 = @intCast(i % ATLAS_COLS);
        const row: u16 = @intCast(i / ATLAS_COLS);
        const gx: usize = col * GLYPH_W;
        const gy: usize = row * GLYPH_H;

        for (0..GLYPH_H) |py| {
            const scanline = bits[py];
            for (0..GLYPH_W) |px| {
                const bit = (scanline >> @intCast(7 - px)) & 1;
                if (bit == 0) continue;
                const off = (gy + py) * @as(usize, ATLAS_W) * 4 + (gx + px) * 4;
                @as(*align(1) u32, @ptrCast(&atlas_pixels[off])).* = 0xFFFFFFFF;
            }
        }
    }

    return FontAtlas.loadFromBitmap(
        allocator,
        atlas_pixels,
        ATLAS_W,
        ATLAS_H,
        GLYPH_W,
        GLYPH_H,
    );
}

// --- Tests ---

test "createDefaultFont correct size and metrics" {
    var font = try createDefaultFont(std.testing.allocator);
    defer font.deinit();

    try std.testing.expectEqual(@as(u16, ATLAS_W), font.atlas_w);
    try std.testing.expectEqual(@as(u16, ATLAS_H), font.atlas_h);
    try std.testing.expectEqual(@as(u16, GLYPH_H), font.glyph_h);
    // 'A' is codepoint 0x41, glyph index 0x41-0x20 = 33
    try std.testing.expectEqual(@as(u8, GLYPH_W), font.glyphs[0x41 - 0x20].width);
}

test "createDefaultFont 'A' glyph has set pixels" {
    var font = try createDefaultFont(std.testing.allocator);
    defer font.deinit();

    // 'A' glyph index 33; row 2 (33/16=2), col 1 (33%16=1)
    // First row of 'A' bits is 0x10 = 0001_0000, pixel 4 should be set
    const g = font.glyphs[0x41 - 0x20];
    const atlas_stride: usize = ATLAS_W * 4;
    // Check at least one pixel in the glyph is white/non-zero
    var found_set = false;
    for (0..GLYPH_H) |row| {
        for (0..GLYPH_W) |col| {
            const off = (@as(usize, g.y) + row) * atlas_stride + (@as(usize, g.x) + col) * 4;
            if (font.pixels[off + 3] == 255) {
                found_set = true;
                break;
            }
        }
        if (found_set) break;
    }
    try std.testing.expect(found_set);
}
